package Code2Teams;
/**
 *The Spymaster class simply gets or sets the team of the spymaster.
 */
public class Spymaster {
	private String Team;	
	/**
	 * Returns the Team of the Spymaster
	 * @return the Spymaster's team.
	 */
	public String getTeam() {
		return Team;
	}
	/**
	 * Set's the team of the Spymaster.
	 * @param team - String of the team's name
	 */
	public void setTeam(String team) {
		Team = team;
	}	
}
